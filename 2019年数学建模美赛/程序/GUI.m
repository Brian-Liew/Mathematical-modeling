% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
data1=get(handles.listbox1,'string');
if strcmp(data1,'随机坐标显示框')~=1
    data2=str2num(data1);%这里直接转换就行，直接转换成对应的坐标矩阵
    t1=clock;
    bestLu=SATSP(data2);
    t2=clock;
    t=etime(t2,t1);
    set(handles.listbox2,'string',{['运算完成，运行时间为：',num2str(t),'秒'],['最短路径长度：',num2str(bestLu),'(长度单位)']})%字符串拼接，并可以分行显示
else
    set(handles.listbox2,'string','请首先生成随机坐标')
end


